    private void AddBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBActionPerformed
        if(IDTF.getText().equals("") || FTF.getText().equals("") || LTF.getText().equals("") || EmailTF.getText().equals("") || PhoneTF.getText().equals("") || AddressTF.getText().equals("")){
        JOptionPane.showMessageDialog(this, "Please enter All Data! ");
        }else {
            String data[] = {IDTF.getText(),FTF.getText(),LTF.getText(),EmailTF.getText(),PhoneTF.getText(),AddressTF.getText()};
            DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
            //add string array data
           tblModel.addRow(data);
           
           JOptionPane.showMessageDialog(this, "Add Data Successfully ");
           IDTF.setText("");
           FTF.setText("");
           LTF.setText("");
           EmailTF.setText("");
           PhoneTF.setText("");
           AddressTF.setText("");
        }
    }//GEN-LAST:event_AddBActionPerformed

