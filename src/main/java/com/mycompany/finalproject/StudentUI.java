    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        DefaultTableModel tblModel= (DefaultTableModel) jTable1.getModel();
        
        String tblstudentID = tblModel.getValueAt(jTable1.getSelectedRow(), 0).toString();
        String tblfirstName = tblModel.getValueAt(jTable1.getSelectedRow(), 1).toString();
        String tbllastName = tblModel.getValueAt(jTable1.getSelectedRow(), 2).toString();
        String tblemail = tblModel.getValueAt(jTable1.getSelectedRow(), 3).toString();
        String tblphone = tblModel.getValueAt(jTable1.getSelectedRow(), 4).toString();
        String tbladdress = tblModel.getValueAt(jTable1.getSelectedRow(), 5).toString();
        
        // set to textfield 
        
        IDTF.setText(tblstudentID);
        FTF.setText(tblfirstName);
        LTF.setText(tbllastName);
        EmailTF.setText(tblemail);
        PhoneTF.setText(tblphone);
        AddressTF.setText(tbladdress);
    }//GEN-LAST:event_jTable1MouseClicked

    private void DeleteBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteBActionPerformed
       DefaultTableModel tblModel = (DefaultTableModel) jTable1.getModel();
       
       //delete Row 
       if (jTable1.getSelectedRowCount()== 1){
              IDTF.setText("");
           FTF.setText("");
           LTF.setText("");
           EmailTF.setText("");
           PhoneTF.setText("");
           AddressTF.setText("");
           // if single row selectes 
           tblModel.removeRow(jTable1.getSelectedRow());
           
       }else {
           if(jTable1.getRowCount() == 0 ){
               JOptionPane.showMessageDialog(this, "Table is Empty");
               
           }else {
               JOptionPane.showMessageDialog(this, "Please Enter Single Row For Delete");
               }
             
       }
    }//GEN-LAST:event_DeleteBActionPerformed

